// MARK: - Configuration

struct Configuration {
    let prefix: String
    let outputURL: URL
    let copyright: String
    let createdDate: String
    let creator: String

    init(prefix: String, outputURL: URL) {
        self.prefix = prefix
        self.outputURL = outputURL
        self.copyright = """
        //
        //  \(prefix)UIPrefixAliases.swift
        //  PrismUI
        //
        //  Generated by GeneratePrefixAliases.swift
        //

        """
        self.createdDate = DateFormatter.localizedString(from: Date(), dateStyle: .medium, timeStyle: .none)
        self.creator = "PrismUI Prefix Generator"
    }
}

