// MARK: - Generator

class AliasGenerator {
    private let config: Configuration

    init(config: Configuration) {
        self.config = config
    }

    func generate() -> String {
        var output = ""

        // Header
        output += "//\n"
        output += "//  \(config.prefix)UIPrefixAliases.swift\n"
        output += "//  PrismUI\n"
        output += "//\n"
        output += "//  Generated by \(config.creator) on \(config.createdDate).\n"
        output += "//\n"
        output += "//  Arquivo de typealiases para personalização de prefixo do Design System.\n"
        output += "//  Este arquivo foi gerado automaticamente pelo script GeneratePrefixAliases.swift\n"
        output += "//\n"
        output += "//  PARA USAR: Importe este arquivo no seu projeto e use os aliases com prefixo '\(config.prefix)'\n"
        output += "//\n"
        output += "//  Exemplo:\n"
        output += "//  ```swift\n"
        output += "//  import PrismUI\n"
        output += "//\n"
        output += "//  var button: \(config.prefix)Button\n"
        output += "//  var text: \(config.prefix)Text\n"
        output += "//  ```\n"
        output += "//\n"
        output += "\n"
        output += "import SwiftUI\n"
        output += "\n"

        // Group types by category
        let groupedTypes = Dictionary(grouping: TypeDefinition.allTypes) { $0.category }

        // Generate typealiases for each category
        for category in groupedTypes.keys.sorted() {
            let types = groupedTypes[category]!
            output += "// MARK: - \(category)\n"
            output += "\n"

            for type in types {
                let aliasName = type.name.replacingOccurrences(of: "Prism", with: config.prefix)
                output += "public typealias \(aliasName) = \(type.name)\n"
            }

            output += "\n"
        }

        // View Modifiers Extensions
        output += "// MARK: - View Modifiers Extensions\n"
        output += "\n"
        output += "extension View {\n"
        output += "    // MARK: - Accessibility\n"
        output += "\n"
        output += "    /// Aplica prefixo personalizado às propriedades de acessibilidade\n"
        output += "    public func \(config.prefix.lowercased())(accessibility properties: PrismAccessibilityProperties) -> some View {\n"
        output += "        prism(accessibility: properties)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Atalho para definir apenas testID com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(testID: String) -> some View {\n"
        output += "        prism(testID: testID)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica propriedades de acessibilidade usando builder pattern com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(accessibility builder: (PrismAccessibilityConfig) -> PrismAccessibilityConfig) -> some View {\n"
        output += "        prism(accessibility: builder)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Style Modifiers\n"
        output += "\n"
        output += "    /// Aplica cor de fundo com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(background style: PrismColor) -> some View {\n"
        output += "        prism(background: style)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica cor de tint com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(tint color: PrismColor) -> some View {\n"
        output += "        prism(tint: color)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica cor de foreground com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(color: PrismColor) -> some View {\n"
        output += "        prism(color: color)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Environment Modifiers\n"
        output += "\n"
        output += "    /// Aplica tema com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(theme: PrismThemeProtocol) -> some View {\n"
        output += "        prism(theme: theme)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica locale com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(locale: PrismLocale) -> some View {\n"
        output += "        prism(locale: locale)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica color scheme com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(colorScheme: ColorScheme? = nil) -> some View {\n"
        output += "        prism(colorScheme: colorScheme)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica estado de loading com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(loading: Bool) -> some View {\n"
        output += "        prism(loading: loading)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica estado de disabled com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(disabled: Bool) -> some View {\n"
        output += "        prism(disabled: disabled)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Text Modifiers\n"
        output += "\n"
        output += "    /// Aplica alinhamento de texto com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(alignment: TextAlignment) -> some View {\n"
        output += "        prism(alignment: alignment)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica fonte com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(\n"
        output += "        font: Font = .body,\n"
        output += "        weight: Font.Weight? = nil,\n"
        output += "        design: Font.Design? = nil\n"
