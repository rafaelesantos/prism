        output += "        effect: T,\n"
        output += "        options: SymbolEffectOptions = .default,\n"
        output += "        isActive: Bool = true\n"
        output += "    ) -> some View {\n"
        output += "        prismSymbol(effect: effect, options: options, isActive: isActive)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica efeito skeleton com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())Skeleton() -> some View {\n"
        output += "        prismSkeleton()\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica efeito parallax com prefixo personalizado (iOS apenas)\n"
        output += "    public func \(config.prefix.lowercased())Parallax(width: PrismSize? = nil, height: PrismSize?) -> some View {\n"
        output += "        prismParallax(width: width, height: height)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica efeito de confetti com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())Confetti(\n"
        output += "        amount: Int = 30,\n"
        output += "        seconds: Int = 4,\n"
        output += "        isActive: Bool\n"
        output += "    ) -> some View {\n"
        output += "        prismConfetti(amount: amount, seconds: seconds, isActive: isActive)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Shape & Clip Modifiers\n"
        output += "\n"
        output += "    /// Aplica clip de shape com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())(clip shape: PrismShape) -> some View {\n"
        output += "        prism(clip: shape)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Screen & Display Modifiers\n"
        output += "\n"
        output += "    /// Aplica observação de screen com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())ScreenObserve(minimumWidthScreen: CGFloat = 430) -> some View {\n"
        output += "        prismScreenObserve(minimumWidthScreen: minimumWidthScreen)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica browser sheet com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())Browser(url: Binding<URL?>) -> some View {\n"
        output += "        prismBrowser(url: url)\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Preview Modifiers\n"
        output += "\n"
        output += "    /// Aplica preview com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())Preview(\n"
        output += "        layout: PreviewLayout,\n"
        output += "        orientation: InterfaceOrientation,\n"
        output += "        colorScheme: ColorScheme,\n"
        output += "        locale: PrismLocale\n"
        output += "    ) -> some View {\n"
        output += "        prismPreview(\n"
        output += "            layout: layout,\n"
        output += "            orientation: orientation,\n"
        output += "            colorScheme: colorScheme,\n"
        output += "            locale: locale\n"
        output += "        )\n"
        output += "    }\n"
        output += "\n"
        output += "    // MARK: - Conditional Modifiers\n"
        output += "\n"
        output += "    /// Aplica transformação condicional com prefixo personalizado\n"
        output += "    public func \(config.prefix.lowercased())<Content: View>(\n"
        output += "        if condition: Bool,\n"
        output += "        transition: AnyTransition = .scale,\n"
        output += "        animation: Animation? = .linear,\n"
        output += "        transform: (Self) -> Content\n"
        output += "    ) -> some View {\n"
        output += "        prism(if: condition, transition: transition, animation: animation, transform: transform)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica transformação condicional com item opcional\n"
        output += "    public func \(config.prefix.lowercased())<Content: View, Value>(\n"
        output += "        item value: Value?,\n"
        output += "        transform: (Self, Value) -> Content\n"
        output += "    ) -> some View {\n"
        output += "        prism(item: value, transform: transform)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica transformação condicional com else\n"
        output += "    public func \(config.prefix.lowercased())<Content: View, ElseContent: View>(\n"
        output += "        if condition: Bool,\n"
        output += "        transform: (Self) -> Content,\n"
        output += "        `else`: ((Self) -> ElseContent)? = nil\n"
        output += "    ) -> some View {\n"
        output += "        prism(if: condition, transform: transform, else: `else`)\n"
        output += "    }\n"
        output += "\n"
        output += "    /// Aplica transformação condicional com item opcional e else\n"
        output += "    public func \(config.prefix.lowercased())<Content: View, Value, ElseContent: View>(\n"
        output += "        item value: Value?,\n"
        output += "        transform: (Self, Value) -> Content,\n"
        output += "        `else`: ((Self) -> ElseContent)? = nil\n"
        output += "    ) -> some View {\n"
        output += "        prism(item: value, transform: transform, else: `else`)\n"
        output += "    }\n"
        output += "}\n"

        return output
    }

    func write() throws {
        let directory = config.outputURL.deletingLastPathComponent()
        try FileManager.default.createDirectory(at: directory, withIntermediateDirectories: true)

        let content = generate()
        try content.write(to: config.outputURL, atomically: true, encoding: .utf8)

        print("✅ Arquivo gerado com sucesso: \(config.outputURL.path)")
        print("📊 Total de typealiases: \(TypeDefinition.allTypes.count)")
    }
}

// MARK: - Main

func main() {
    let arguments = CommandLine.arguments

    guard arguments.count >= 2 else {
        print("❌ Uso: swift GeneratePrefixAliases.swift <PREFIX> [OUTPUT_PATH]")
        print("")
        print("Exemplos:")
        print("  swift GeneratePrefixAliases.swift Zenith")
        print("  swift GeneratePrefixAliases.swift App ./MyApp/Aliases/AppPrefixAliases.swift")
        print("")
        print("O arquivo será gerado em:")
        print("  ./Sources/PrismUI/Exports/<PREFIX>UIPrefixAliases.swift (padrão)")
        exit(1)
    }

    let prefix = arguments[1]
    let outputPath: String

    if arguments.count >= 3 {
        outputPath = arguments[2]
    } else {
        // Caminho relativo ao diretório atual
        outputPath = "./\(prefix)UIPrefixAliases.swift"
    }

    let outputURL = URL(fileURLWithPath: outputPath)

    print("🚀 Gerando typealiases com prefixo '\(prefix)'...")
    print("📁 Output: \(outputURL.path)")
    print("")

    let config = Configuration(prefix: prefix, outputURL: outputURL)
    let generator = AliasGenerator(config: config)

    do {
        try generator.write()
    } catch {
        print("❌ Erro ao gerar arquivo: \(error)")
        exit(1)
    }
}

main()
